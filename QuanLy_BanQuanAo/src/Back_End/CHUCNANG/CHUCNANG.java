public class CHUCNANG {
	private String tenCN, moTa;
	 
	public CHUCNANG()
	 {
		 
	 }

	public CHUCNANG(String tenCN, String moTa) {
		super();
		this.tenCN = tenCN;
		this.moTa = moTa;
	}

	public String getTenCN() {
		return tenCN;
	}

	public void setTenCN(String tenCN) {
		this.tenCN = tenCN;
	}

	public String getMoTa() {
		return moTa;
	}

	public void setMoTa(String moTa) {
		this.moTa = moTa;
	}
}
	
